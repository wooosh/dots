-- mapping of class to hotkey list
local ff_rules = {
    URxvt = {"a", 's', 'd'},
    ['Google-chrome'] = {'g', 't'},
    ['Code - OSS'] = {'q'}
}

-- TODO: just try list of clients
-- mapping of hotkey = windowid
local ff_hotkeys = {}

local function ff_get_hotkey(client)
    for k,v in pairs(ff_hotkeys) do
        if v==client.window then 
            return k
         end
      end
    return "?"
end

-- TODO: onclose remove client
local function ff_remove_client(client)
    local hotkey = ff_get_hotkey(client)
    if hotkey == "?" then return end

    ff_hotkeys[hotkey] = nil

    for _, c in ipairs(client.get()) do
        if c.window == client.window then
            client:emit_signal("fastfocus-update")
        end
    end
end

local function ff_auto_assign(client)
    -- check if client is already registered
    if ff_get_hotkey(client) ~= "?" then 
        return
    end

    if ff_rules[client.class] ~= nil then
        local hotkeys = ff_rules[client.class]
        for _, v in ipairs(hotkeys) do
            if ff_hotkeys[v] == nil then
                    ff_hotkeys[v] = client.window
                    break
            end
            
        end
        client:emit_signal("fastfocus-update")
    end
end


