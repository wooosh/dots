local function ff_label_widget(client)
    
    local widget = wibox.widget {
        markup = '<b>' .. ff_get_hotkey(client) .. '</b>',
        widget = wibox.widget.textbox
    }
    client:connect_signal("fastfocus-update", function()
       widget.markup = '<b>' .. ff_get_hotkey(client) .. '</b>'
    end)

    return widget
end

